const endpoints = {
    plan: "/api/study-plan",
    quiz: "/api/quiz",
    summary: "/api/upload-summary",
    chat: "/api/chat",
    flashcards: "/api/flashcards",
    revision: "/api/revision-plan",
};

const hasMarked = typeof marked !== "undefined";
const hasPurify = typeof DOMPurify !== "undefined";

if (hasMarked) {
    marked.setOptions({
        breaks: true,
        gfm: true,
    });
}

function escapeHtml(text) {
    return String(text || "")
        .replaceAll("&", "&amp;")
        .replaceAll("<", "&lt;")
        .replaceAll(">", "&gt;")
        .replaceAll('"', "&quot;")
        .replaceAll("'", "&#39;");
}

function sanitizeHtml(input) {
    if (hasPurify) {
        return DOMPurify.sanitize(input);
    }
    return input;
}

function markdownHtml(markdownText) {
    if (!hasMarked) {
        return `<pre>${escapeHtml(markdownText || "")}</pre>`;
    }

    const raw = marked.parse(markdownText || "");
    return sanitizeHtml(raw);
}

function renderResult(containerId, markdownText, modelName) {
    const container = document.getElementById(containerId);
    if (!container) return;

    container.innerHTML = `
        <article class="ai-content">${markdownHtml(markdownText)}</article>
        <div class="meta">Generated by: ${modelName || "Gemini"}</div>
    `;
}

function renderError(containerId, message) {
    const container = document.getElementById(containerId);
    if (!container) return;

    container.innerHTML = `<p class="placeholder">${sanitizeHtml(message)}</p>`;
}

function plainTextFromContainer(containerId) {
    const container = document.getElementById(containerId);
    if (!container) return "";
    return container.innerText.trim();
}

function downloadText(filename, content) {
    const blob = new Blob([content], { type: "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = filename;
    document.body.appendChild(link);
    link.click();
    link.remove();
    URL.revokeObjectURL(url);
}

function setButtonLoading(form, isLoading, loadingText) {
    const button = form.querySelector("button[type='submit']");
    if (!button) return;

    if (!button.dataset.originalText) {
        button.dataset.originalText = button.textContent;
    }

    button.disabled = isLoading;
    button.textContent = isLoading ? loadingText : button.dataset.originalText;
}

async function parseApiResponse(response) {
    let payload;
    try {
        payload = await response.json();
    } catch {
        payload = { error: "Invalid server response." };
    }

    if (!response.ok) {
        throw new Error(payload.error || "Something went wrong.");
    }

    return payload;
}

async function postJson(url, body) {
    const response = await fetch(url, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(body),
    });
    return parseApiResponse(response);
}

async function postFormData(url, formData) {
    const response = await fetch(url, {
        method: "POST",
        body: formData,
    });
    return parseApiResponse(response);
}

function appendChat(role, content, isMarkdown = false, modelName = "") {
    const box = document.getElementById("chat-output");
    if (!box) return;

    const placeholder = box.querySelector(".placeholder");
    if (placeholder) placeholder.remove();

    const item = document.createElement("div");
    item.className = `chat-item ${role}`;

    const bubble = document.createElement("div");
    bubble.className = "bubble";

    if (isMarkdown) {
        bubble.innerHTML = markdownHtml(content);
    } else {
        bubble.textContent = content;
    }

    item.appendChild(bubble);
    box.appendChild(item);

    if (modelName) {
        const meta = document.createElement("div");
        meta.className = "meta";
        meta.textContent = `Generated by: ${modelName}`;
        box.appendChild(meta);
    }

    box.scrollTop = box.scrollHeight;
}

document.querySelectorAll("[data-download-target]").forEach((button) => {
    button.addEventListener("click", () => {
        const targetId = button.getAttribute("data-download-target");
        const fileName = button.getAttribute("data-download-name") || "study-output.md";
        const content = plainTextFromContainer(targetId);

        if (!content) {
            return;
        }

        downloadText(fileName, content);
    });
});

const planForm = document.getElementById("plan-form");
if (planForm) {
    planForm.addEventListener("submit", async (event) => {
        event.preventDefault();
        setButtonLoading(planForm, true, "Generating Plan...");

        try {
            const formData = new FormData(planForm);
            const payload = {
                goal: formData.get("goal"),
                currentLevel: formData.get("currentLevel"),
                hoursPerDay: Number(formData.get("hoursPerDay")),
                daysPerWeek: Number(formData.get("daysPerWeek")),
                targetDate: formData.get("targetDate"),
                focusAreas: formData.get("focusAreas"),
                notes: formData.get("notes"),
            };

            const data = await postJson(endpoints.plan, payload);
            renderResult("plan-output", data.result, data.model);
        } catch (error) {
            renderError("plan-output", error.message || "Unable to generate study plan.");
        } finally {
            setButtonLoading(planForm, false, "");
        }
    });
}

