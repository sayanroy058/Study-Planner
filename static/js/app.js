const summaryForm = document.getElementById("summary-form");
if (summaryForm) {
    summaryForm.addEventListener("submit", async (event) => {
        event.preventDefault();
        setButtonLoading(summaryForm, true, "Summarizing...");

        try {
            const formData = new FormData(summaryForm);
            const uploadedFile = formData.get("file");
            if (!uploadedFile || uploadedFile.size === 0) {
                throw new Error("Please select a file first.");
            }

            const data = await postFormData(endpoints.summary, formData);
            renderResult("summary-output", data.result, data.model);
        } catch (error) {
            renderError("summary-output", error.message || "Unable to summarize document.");
        } finally {
            setButtonLoading(summaryForm, false, "");
        }
    });
}

const chatForm = document.getElementById("chat-form");
if (chatForm) {
    chatForm.addEventListener("submit", async (event) => {
        event.preventDefault();
        setButtonLoading(chatForm, true, "Asking AI...");

        try {
            const formData = new FormData(chatForm);
            const payload = {
                question: String(formData.get("question") || "").trim(),
                context: String(formData.get("context") || "").trim(),
            };

            if (!payload.question) {
                throw new Error("Please enter your question.");
            }

            appendChat("user", payload.question, false);
            const data = await postJson(endpoints.chat, payload);
            appendChat("ai", data.result, true, data.model);
            chatForm.reset();
        } catch (error) {
            appendChat("ai", error.message || "Unable to answer right now.", false);
        } finally {
            setButtonLoading(chatForm, false, "");
        }
    });
}

